name = ""
age = 0
Email = ""
Password = ""

def setname(new_name):
    global name
    name = new_name

def getname():
    return name

def setage(new_age):
    global age
    age = new_age

def getage():
    return age

def setemail(new_email):
    global Email
    Email = new_email

def getemail():
    return Email

def setpassword(new_password):
    global Password
    Password = new_password

def getpassword():
    return Password
