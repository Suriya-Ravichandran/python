def add(value1, value2, value3=0, value4=0):
    print(value1 + value2 + value3 + value4)

# Now you can call the function with different numbers of arguments
add(23, 12)                # Calls with 2 arguments
add(23, 12, 5)             # Calls with 3 arguments
add(23, 12, 5, 7)          # Calls with 4 arguments
